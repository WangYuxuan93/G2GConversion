## if you want to add POS tags into your embedding part, add them as special tokens, then load the adopted BERT
def initialize_robertagraph(BERT_NAME_OR_PATH, layernorm_key=False,layernorm_value=False,input_label_graph=False, input_unlabel_graph=False,label_size=None):

    robertaGconfig = RobertaGraphConfig.from_pretrained(BERT_NAME_OR_PATH)
    # bertgconfig = SemSynRobertaConfig.from_pretrained(BERT_NAME_OR_PATH)
    init_roberta = RobertaModel.from_pretrained(BERT_NAME_OR_PATH)
    robertaGconfig.add_graph_par(layernorm_key,layernorm_value,input_label_graph, input_unlabel_graph,label_size)
    model = RobertaGraphModel(robertaGconfig)
    model.load_state_dict(init_roberta.state_dict(), strict=False)
    return model


